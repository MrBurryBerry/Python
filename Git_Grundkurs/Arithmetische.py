# Rechnungsoperationen

a = 10
b = 5
c = 55

#Adition
summe = a + b
print("Summe:", summe)

# Subtraktion
differenz = c - b - a
print("Differenz:", differenz)

#Multiplikation
produkt = a * b * c
print("Produkt:", produkt)

#Division
divition = c / b
print("Division:", divition)

#Modulus
modulus = c % b
print("Modulus:", modulus)


